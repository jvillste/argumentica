(ns argumentica.db.branch
  (:require  [clojure.test :as t]))


#_(defn indexes-to-index-definitions [indexes]
  (map (fn [key index-map]))
  (reduce (fn [index-definitions index]
            (conj index-definitions
                  ))))

#_(defn [base-indexes base-transaction-number]
  {:base-indexes base-indexes
   :base-transaction-number base-transaction-number})
